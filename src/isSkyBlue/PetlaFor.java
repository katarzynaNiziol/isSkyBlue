package isSkyBlue;

public class PetlaFor {
    public static void main(String[] args) {
      for (int i = 0, j= 10; i<=10; i++, j-- ) {
            System.out.println(i);
            System.out.println(j);
            System.out.println( );
        }

        int k = 0;
        for (; k < 10; k++) {
            System.out.println(k);
        }
    }
}